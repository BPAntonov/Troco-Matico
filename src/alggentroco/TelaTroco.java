    public void construcaoProblema(){
        try{
            if(this.jTFValorTroco.getText() != ""){
                this.t1.setValortrocoT(Integer.parseInt(this.jTFValorTroco.getText()));
                //System.out.println(this.t1.getValortrocoT());
            }
            
            this.t1.setPorcentagemcrossover(Double.parseDouble(this.jTFPorcentagemCross.getText()));
            this.t1.setPorcentagemmutacao(Double.parseDouble(this.jTFPorcentagemMut.getText()));
            this.t1.setQuantCromossomos(Integer.parseInt(this.jTFQuantCromossomos.getText()));
            this.t1.setInicialAleatorio(Integer.parseInt(this.jTFAleatInicial.getText()));
            
            //this.t1.heurGeracoes(true);
            
            if(this.jTFNumGeracoes.getText().equals("0")){
                this.t1.heurGeracoes(true);
            }else{
                this.t1.setGeracoes(Integer.parseInt(this.jTFNumGeracoes.getText()));
            
            }            
        }catch(Exception e){


        }
    
    
    }
